function show1() {
    let x = document.getElementById("btn1");
    if (x.style.visibility === "hidden"){
        x.style.visibility = "visible";
    } else {
        x.style.visibility = "visible";
    }
}

function hide1() {
    let x = document.getElementById("btn1");
    if(x.style.visibility === "visible"){
        x.style.visibility = "hidden";
    } else {
        x.style.visibility = "hidden";
    }
}

